module.exports = (req, res, next) => {
    if (req.session.isAuthenticated) {
      // Якщо користувач Увійшов, дозволяємо доступ
      next();
    } else {
      // Якщо користувач не увійшов, перенаправляємо його на сторінку помилки або головну сторінку
      console.log('Спроба відкрити маршрут без прав авторизовано користувача')
      res.redirect('/');
    }
  };
  